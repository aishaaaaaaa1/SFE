  function pageName() {
    var parts = (location.pathname || '').split('/');
    return parts.pop() || parts.pop() || '';
  }

  function esc(s) {
    return String(s)
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;')
      .replace(/"/g, '&quot;');
  }

  function normalize(s) {
    return String(s)
      .toLowerCase()
      .normalize('NFD')
      .replace(/[\u0300-\u036f]/g, '');
  }

  function matches(doc, q, typeFilter) {
    if (typeFilter && typeFilter !== 'all' && doc.filterKey !== typeFilter) return false;
    var t = q.trim();
    if (!t) return true;
    var n = normalize(t);
    var hay = normalize(doc.title + ' ' + (doc.meta || ''));
    return hay.indexOf(n) !== -1;
  }

